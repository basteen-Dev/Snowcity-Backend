const comboService = require('../../services/comboService');
const { applyOfferPricing } = require('../../services/offerPricing');

const toNumber = (value, fallback = 0) => {
  const num = Number(value);
  return Number.isFinite(num) ? num : fallback;
};

const resolveComboSlotBasePrice = (slot = {}, combo = {}) => {
  return (
    toNumber(slot.base_price, 0) ||
    toNumber(slot.price, 0) ||
    toNumber(combo?.pricing?.base_price, 0) ||
    toNumber(
      combo?.combo_price ??
        combo?.price ??
        combo?.total_price ??
        combo?.starting_price ??
        combo?.min_price ??
        0,
      0
    )
  );
};

const normalizeComboSlotId = (slot = {}) => {
  if (!slot?.combo_slot_id) return null;
  const numeric = Number(slot.combo_slot_id);
  return Number.isFinite(numeric) ? numeric : null;
};

const enrichComboSlotWithPricing = async (slot, combo) => {
  if (!slot || !combo) return slot;
  const basePrice = resolveComboSlotBasePrice(slot, combo);
  if (!basePrice) {
    return { ...slot, base_price: 0, price: 0 };
  }

  const offerPricing = await applyOfferPricing({
    targetType: 'combo',
    targetId: combo.combo_id || combo.id,
    slotType: 'combo',
    slotId: normalizeComboSlotId(slot),
    baseAmount: basePrice,
    booking_date: slot.start_date || slot.date || null,
    booking_time: slot.start_time || null,
  });

  return {
    ...slot,
    base_price: basePrice,
    price: offerPricing.unit,
    offer: offerPricing.offer,
    offer_discount: offerPricing.discount,
    offer_discount_percent: offerPricing.discount_percent,
    pricing: {
      base_price: basePrice,
      final_price: offerPricing.unit,
      discount_amount: offerPricing.discount,
      discount_percent: offerPricing.discount_percent,
      offer: offerPricing.offer,
    },
  };
};

const mapComboSlotsWithPricing = async (slots = [], combo = null) => {
  if (!combo || !Array.isArray(slots) || !slots.length) return slots;
  return Promise.all(slots.map((slot) => enrichComboSlotWithPricing(slot, combo)));
};

// GET /api/combos
exports.listCombos = async (req, res, next) => {
  try {
    const active = req.query.active === undefined ? null : String(req.query.active).toLowerCase() === 'true';
    const data = await comboService.list({ active });
    res.json({ data, meta: { count: data.length } });
  } catch (err) {
    next(err);
  }
};

// GET /api/combos/:id
exports.getComboById = async (req, res, next) => {
  try {
    const id = Number(req.params.id);
    const row = await comboService.getById(id);
    res.json(row);
  } catch (err) {
    next(err);
  }
};

// GET /api/combos/:id/slots?date=YYYY-MM-DD
exports.getComboSlots = async (req, res, next) => {
  try {
    const comboId = Number(req.params.id);
    const { date } = req.query;
    
    console.log('üéØ Combo Slots API called:', { comboId, date });
    
    if (!comboId) {
      return res.status(400).json({ error: 'combo_id is required' });
    }
    
    if (!date) {
      return res.status(400).json({ error: 'date is required' });
    }
    
    // Get combo details
    const combo = await comboService.getById(comboId);
    console.log('üìã Combo found:', !!combo, combo?.name);
    
    if (!combo) {
      return res.status(404).json({ error: 'Combo not found' });
    }
    
    // Calculate slot duration based on number of attractions
    const attractionCount = combo.attraction_ids ? combo.attraction_ids.length : 1;
    const slotDuration = Math.max(attractionCount, 1); // At least 1 hour
    
    console.log('üî¢ Slot details:', { attractionCount, slotDuration });
    
    // Import the slot generation function
    const { generateDynamicSlotsForDateRange } = require('../../models/comboSlots.model');
    
    // Generate slots for the single date
    const startDate = new Date(date);
    const endDate = new Date(date); // Same day
    
    console.log('üìÖ Generating slots for:', { startDate, endDate });
    
    const slots = generateDynamicSlotsForDateRange(
      comboId, 
      startDate, 
      endDate,
      slotDuration
    );
    
    console.log('üé∞ Slots generated:', slots.length);
    
    // Include combo details in the response
    const slotsWithComboDetails = slots.map(slot => ({
      ...slot,
      combo_name: combo.name,
      combo_details: {
        name: combo.name,
        attraction_count: attractionCount,
        slot_duration: slotDuration,
        total_price: combo.total_price
      }
    }));
    
    const slotsWithPricing = await mapComboSlotsWithPricing(slotsWithComboDetails, combo);

    const response = { 
      data: slotsWithPricing, 
      meta: { 
        count: slots.length,
        combo: {
          id: combo.combo_id || combo.id,
          name: combo.name,
          attraction_count: attractionCount,
          slot_duration: slotDuration
        }
      } 
    };
    
    console.log('‚úÖ Response prepared:', response.meta.count, 'slots');
    res.json(response);
  } catch (err) {
    console.error('‚ùå Combo slots error:', err);
    next(err);
  }
};